package calculadora;

public class Calculadora {
	
	//atributos
	double n1,n2;
	
	
	//metodos
	double soma(double v1, double v2) {
	return v1 + v2;	
	}
	
	double sub(double v1, double v2) {
		return v1 - v2;	
	}
	
	double div(double v1, double v2) {	
	return v1 / v2;	
	}
	
	double multi(double v1, double v2) {
	return v1 * v2;	
	}
	
	double resto(double v1, double v2) {
	return v1 % v2;
	}
	
}